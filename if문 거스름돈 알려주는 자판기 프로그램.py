#if문 거스름돈 알려주는 자판기 프로그램
print("""1.아메리카노 :1800원
2.까페라떼 :2700
3.핫초코 :2300""")

c=int(input("뭐 마실래"))

if c==1:
  c=1800
elif c==2:
  c=2700
elif c==3:
  c=2300
a=int(input("몇잔?"))

b=a*c
print("총 금액은", b, "입니다 돈을 넣어주세요")
d=int(input("금액 입력"))
e=d-b
f=e//1000
g=(e%1000)/500
h=(e%1000)%500
i=h//100
print("거스름돈은", e, "입니다 가져가세요")
print("천원짜리",int(f),"장","500원짜리",int(g),"개","100원짜리",int(i),"개")
